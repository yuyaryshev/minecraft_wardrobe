    private final Screen parent;
    private boolean useRussian = false;
    private int pageIndex = 0;
    private Button prevButton;
    private Button nextButton;
    private List<String> enPages = List.of(EN_FALLBACK);
    private List<String> ruPages = List.of(RU_FALLBACK);

    public WardrobeHowToScreen(Screen parent) {
        super(Component.literal("How to?"));
        this.parent = parent;
    }

    @Override
    protected void init() {
        int buttonY = height - 28;
        prevButton = addRenderableWidget(Button.builder(Component.literal("Prev"), button -> {
            pageIndex = Math.max(0, pageIndex - 1);
        }).bounds(width / 2 - 120, buttonY, 40, 20).build());
        nextButton = addRenderableWidget(Button.builder(Component.literal("Next"), button -> {
            pageIndex = Math.min(getPageCount() - 1, pageIndex + 1);
        }).bounds(width / 2 - 76, buttonY, 40, 20).build());
        addRenderableWidget(Button.builder(Component.literal("EN"), button -> {
            useRussian = false;
        }).bounds(width / 2 - 64, buttonY, 30, 20).build());
        addRenderableWidget(Button.builder(Component.literal("RU"), button -> {
            useRussian = true;
        }).bounds(width / 2 - 30, buttonY, 30, 20).build());
        addRenderableWidget(Button.builder(Component.literal("Done"), button -> minecraft.setScreen(parent))
                .bounds(width / 2 + 8, buttonY, 60, 20)
                .build());

        enPages = loadPages("howto_en.txt", EN_FALLBACK);
        ruPages = loadPages("howto_ru.txt", RU_FALLBACK);
    }

    @Override
    public void render(GuiGraphics guiGraphics, int mouseX, int mouseY, float partialTick) {
        renderBackground(guiGraphics);
        guiGraphics.drawString(font, title, PADDING, PADDING - 2, 0xFFFFFFFF, false);

        prevButton.active = pageIndex > 0;
        nextButton.active = pageIndex < getPageCount() - 1;
        String text = useRussian ? ruPages.get(pageIndex) : enPages.get(pageIndex);
        List<FormattedCharSequence> lines = font.split(Component.literal(text), width - PADDING * 2);
        int y = PADDING + 12;
        int lineHeight = font.lineHeight + 1;
        for (FormattedCharSequence line : lines) {
            if (y > height - 40) {
                break;
            }
            guiGraphics.drawString(font, line, PADDING, y, 0xFFE6E6E6, false);
            y += lineHeight;
        }
        String pageText = (pageIndex + 1) + "/" + getPageCount();
        guiGraphics.drawString(font, pageText, width - PADDING - font.width(pageText), PADDING - 2, 0xFFAAAAAA, false);
        super.render(guiGraphics, mouseX, mouseY, partialTick);
    }

    @Override
    public boolean shouldCloseOnEsc() {
        return true;
    }

    private int getPageCount() {
        return Math.min(enPages.size(), ruPages.size());
    }

    private List<String> loadPages(String path, String[] fallback) {
        if (minecraft == null) {
            return List.of(fallback);
        }
        try {
            ResourceLocation location = new ResourceLocation("yy_wardrobe", path);
            Optional<Resource> resource = minecraft.getResourceManager().getResource(location);
            if (resource.isEmpty()) {
                return List.of(fallback);
            }
            String content = new String(resource.get().open().readAllBytes(), StandardCharsets.UTF_8);
            String[] parts = content.split("\\R---\\R");
            if (parts.length == 0) {
                return List.of(fallback);
            }
            return Arrays.asList(parts);
        } catch (Exception ex) {
            return List.of(fallback);
        }
    }
